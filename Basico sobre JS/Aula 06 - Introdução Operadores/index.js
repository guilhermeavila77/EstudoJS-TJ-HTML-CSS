/*Operadores basicos
    + adição e concatenação
    - subtração
    * multiplicação
    / divisão
    ** potenciação
*/

//Incremento
let contador = 1;
console.log(contador);

contador = contador + 1;
console.log(contador);
// ou
contador++;
console.log(contador);
// ou 
contador += 1;
console.log(contador);

//Decremento
let contaMenos = 1;
console.log(contaMenos);

contaMenos = contaMenos - 1;
console.log(contaMenos);
// ou
contaMenos--;
console.log(contaMenos);
// ou 
contaMenos -= 1;
console.log(contaMenos);

//Operações com string
const num1 = 10;
const num2 = '5';
console.log(num1 + num2);

//Corrigindo
const num3 = Number('5');
console.log(num1 + num3);